    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       Banco dao = new Banco();
       String insert = "INSERT into cadproprietario (prop_nome,prop_endereco, prop_cpf) values ('"
            +campoNome.getText()+"','"+campoEndereco.getText()+"','"+campoCPF.getText()+"')";
       if(dao.inserir(insert)== true)
       {
           
            campoID.setText("");
            campoCPF.setText("");
            campoNome.setText("");
            campoEndereco.setText("");
           
            JOptionPane.showMessageDialog(rootPane, "Cadastro concluido com sucesso!");
       }
       else
       {
            JOptionPane.showMessageDialog(rootPane, "Erro!");
       }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
       
        String update = "UPDATE cadproprietario set  prop_nome = '"
                +campoNome.getText()+"', prop_endereco ='"+campoEndereco.getText()+"',prop_cpf = '"
                +campoCPF.getText()+"' where id_prop ="
                +Integer.parseInt(campoID.getText());
        Banco dao = new Banco();
        if(dao.inserir(update)== true)
       {
           
            campoID.setText("");
            campoCPF.setText("");
            campoNome.setText("");
            campoEndereco.setText("");
           
            JOptionPane.showMessageDialog(rootPane, "Cadastro alterado com sucesso!");
       }
       else
       {
            JOptionPane.showMessageDialog(rootPane, "Erro!");
       }
    }//GEN-LAST:event_btnAlterarActionPerformed

