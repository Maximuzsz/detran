    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       Proprietario prop = new Proprietario();
       prop.setCpf(campoCPF.getText());
       Banco dao = new Banco();
       prop = dao.busca(prop);
       if(prop.getNomeProprietario() == null){
           JOptionPane.showMessageDialog(rootPane, "Proprietário não cadastrado!");
            campoCPF.setText("");
       }
       else
       {
            campoID.setText(Integer.toString(prop.getIdProprietario()));
            campoNome.setText(prop.getNomeProprietario());
            campoEndereco.setText(prop.getEndereco());
       }
    }//GEN-LAST:event_btnBuscarActionPerformed


