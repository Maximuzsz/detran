    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        Veiculo veiculo = new Veiculo();
        String pl= campoPlaca.getText();
        Banco dao = new Banco();
        veiculo = dao.buscaVeiculo(pl);
        
        if(veiculo.getModelo() == null)
        {
            JOptionPane.showMessageDialog(rootPane, "Veículo não cadastrado!");
            campoPlaca.setText("");
        }
        else
        {
            campoID.setText(Integer.toString(veiculo.getIdveic()));
            campoModelo.setText(veiculo.getModelo());
            campoMarca.setText(veiculo.getMarca());
            campoCor.setText(veiculo.getCor());
            jLabel1.setIcon(new ImageIcon(veiculo.getImagem()));
            pl = dao.bCpf(veiculo.getIdprop());
            campoCPF.setText(pl);
            
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


