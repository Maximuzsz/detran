    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
       
       Banco dao = new Banco();
       Proprietario veiculo = new Proprietario(); 
       veiculo.setCpf(campoCPF.getText());
       veiculo = dao.busca(veiculo);
       if(veiculo.getIdProprietario()== 0)
       {
            JOptionPane.showMessageDialog(rootPane, "CPF inválido!");
           
       }
       else
       {
          String insert = "INSERT into cadveiculo (idprop_veic,placa_veic, modelo_veic,  marca_veic,cor_veic, imagem_veic) values ("
            +veiculo.getIdProprietario()+",'"+campoPlaca.getText()+"','"+campoModelo.getText()+"','"
                  +campoMarca.getText()+"','"+campoCor.getText()+"',?)";
       
            if(dao.cadVeic(insert,ManipularImagem.getImgBytes(imagem)))
            {
                
                campoPlaca.setText("");
                campoID.setText("");
                campoModelo.setText("");
                campoMarca.setText("");
                campoCor.setText("");
                jLabel1.setIcon(new ImageIcon());
                campoCPF.setText("");
                JOptionPane.showMessageDialog(rootPane, "Cadastro concluido com sucesso!");

            }
             else
             {
                 JOptionPane.showMessageDialog(rootPane, "Cadastro não enviado!");
             }  
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        JFileChooser fc = new JFileChooser();
        int res = fc.showOpenDialog(null);

        if (res == JFileChooser.APPROVE_OPTION) {
            File arquivo = fc.getSelectedFile();

            try {
                imagem = ManipularImagem.setImagemDimensao(arquivo.getAbsolutePath(), 160, 160);

                jLabel1.setIcon(new ImageIcon(imagem));

            } catch (Exception ex) {
               // System.out.println(ex.printStackTrace().toString());
            }

        } else {
            JOptionPane.showMessageDialog(null, "Voce nao selecionou nenhum arquivo.");
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        
        Banco dao = new Banco();
        Proprietario veiculo = new Proprietario();
        veiculo.setCpf(campoCPF.getText());
        veiculo = dao.busca(veiculo);
       
        
        
         if(veiculo.getIdProprietario()== 0)
       {
            JOptionPane.showMessageDialog(rootPane, "CPF inválido!");
           
       }
       else
       {
           String sql = "UPDATE cadveiculo set  idprop_veic = '"
                +veiculo.getIdProprietario()+"', placa_veic ='"+campoPlaca.getText()+"', modelo_veic ='"
                +campoModelo.getText()+"', marca_veic = '"
                +campoMarca.getText()+"', cor_veic ='"+campoCor.getText()+"' where id_veic = "
                +Integer.parseInt(campoID.getText());
       
            if(dao.inserir(sql))
            {
                 
                campoPlaca.setText("");
                campoID.setText("");
                campoModelo.setText("");
                campoMarca.setText("");
                campoCor.setText("");
                jLabel1.setIcon(new ImageIcon());
                campoCPF.setText("");
                JOptionPane.showMessageDialog(rootPane, "Cadastro alterado com sucesso!");

            }
             else
             {
                 JOptionPane.showMessageDialog(rootPane, "Cadastro não alterado!");

             }  
       }
         
    }//GEN-LAST:event_jToggleButton2ActionPerformed

